<script>
// Filter functionality
function filterFacturas() {
    const estadoFilter = document.getElementById('estado-filter').value.toLowerCase();
    const clienteFilter = document.getElementById('cliente-filter').value.toLowerCase();
    const rows = document.querySelectorAll('.factura-row');

    rows.forEach(row => {
        const estado = row.getAttribute('data-estado');
        const cliente = row.getAttribute('data-cliente');
        
        const estadoMatch = !estadoFilter || estado === estadoFilter;
        const clienteMatch = !clienteFilter || cliente.includes(clienteFilter);
        
        if (estadoMatch && clienteMatch) {
            row.style.display = '';
        } else {
            row.style.display = 'none';
        }
    });
}

function clearFilters() {
    document.getElementById('estado-filter').value = '';
    document.getElementById('cliente-filter').value = '';
    filterFacturas();
}

// Event listeners
document.getElementById('estado-filter').addEventListener('change', filterFacturas);
document.getElementById('cliente-filter').addEventListener('input', filterFacturas);
</script>
